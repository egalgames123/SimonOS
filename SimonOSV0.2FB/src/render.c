void render_init(uint32_t* fb, uint32_t width, uint32_t height, uint32_t pitch) {
    framebuffer = fb;
    screen_width = width;
    screen_height = height;
    screen_pitch = pitch / 4; // Convert bytes to pixels (assuming 32bpp)

    static uint32_t backbuffer_storage[1024 * 768];  // adjust size if needed
    backbuffer = backbuffer_storage;
}

void draw_pixel(uint32_t x, uint32_t y, uint32_t color) {
    if (x >= screen_width || y >= screen_height || !framebuffer) return;
    backbuffer[y * screen_pitch + x] = color;
}

void draw_rect(uint32_t x, uint32_t y, uint32_t w, uint32_t h, uint32_t color, bool filled) {
    if (!framebuffer) return;
    
    for (uint32_t dy = y; dy < y + h && dy < screen_height; dy++) {
        for (uint32_t dx = x; dx < x + w && dx < screen_width; dx++) {
            if (filled || dx == x || dx == x + w - 1 || dy == y || dy == y + h - 1) {
                backbuffer[dy * screen_pitch + dx] = color;
            }
        }
    }
}

void draw_circle(uint32_t x, uint32_t y, uint32_t r, uint32_t color, bool filled) {
    if (!framebuffer || r == 0) return;
    
    int32_t r2 = (int32_t)(r * r);
    int32_t area = r2 << 2;
    int32_t rr = (int32_t)r << 1;
    int32_t r_minus_1_sq = (int32_t)((r-1)*(r-1));
    
    for (int32_t i = 0; i < area; i++) {
        int32_t tx = (i % rr) - (int32_t)r;
        int32_t ty = (i / rr) - (int32_t)r;
        
        if (tx * tx + ty * ty <= r2) {
            uint32_t px = x + (uint32_t)tx;
            uint32_t py = y + (uint32_t)ty;
            
            if (filled || (tx * tx + ty * ty) >= r_minus_1_sq) {
                if (px < screen_width && py < screen_height) {
                    backbuffer[py * screen_pitch + px] = color;
                }
            }
        }
    }
}

void draw_line(uint32_t x0, uint32_t y0, uint32_t x1, uint32_t y1, uint32_t color) {
    if (!framebuffer) return;
    
    int32_t dx = abs_i32((int32_t)(x1 - x0));
    int32_t dy = abs_i32((int32_t)(y1 - y0));
    int32_t sx = x0 < x1 ? 1 : -1;
    int32_t sy = y0 < y1 ? 1 : -1;
    int32_t err = (dx > dy ? dx : -dy) / 2;
    int32_t e2;
    
    while (1) {
        if (x0 < screen_width && y0 < screen_height) {
            backbuffer[y0 * screen_pitch + x0] = color;
        }
        if (x0 == x1 && y0 == y1) break;
        e2 = err;
        if (e2 > -dx) { err -= dy; x0 += sx; }
        if (e2 < dy) { err += dx; y0 += sy; }
    }
}
void draw_char(char c, uint32_t x, uint32_t y, uint32_t color, uint32_t bg_color, uint32_t scale) {
    if ((unsigned char)c < 32 || (unsigned char)c > 127) return;
    if (scale == 0) scale = 1;
    
    const uint8_t* glyph = font[(unsigned char)c - 32];
    
    for (uint32_t dy = 0; dy < 8; dy++) {
        uint8_t row = glyph[dy];
        
        for (uint32_t dx = 0; dx < 8; dx++) {
            // Changed bit check to (1 << dx) instead of (1 << (7 - dx))
            if (row & (1 << dx)) {
                // Draw scaled character pixel
                for (uint32_t sy = 0; sy < scale; sy++) {
                    for (uint32_t sx = 0; sx < scale; sx++) {
                        draw_pixel(x + dx * scale + sx, y + dy * scale + sy, color);
                    }
                }
            } else if (bg_color != 0xFFFFFFFF) {
                // Draw scaled background pixel
                for (uint32_t sy = 0; sy < scale; sy++) {
                    for (uint32_t sx = 0; sx < scale; sx++) {
                        draw_pixel(x + dx * scale + sx, y + dy * scale + sy, bg_color);
                    }
                }
            }
        }
    }
}

void draw_text(const char* str, uint32_t x, uint32_t y, uint32_t color, uint32_t bg_color, uint32_t scale) {
    uint32_t start_x = x;
    
    while (*str) {
        if (*str == '\n') {
            y += 8 * scale; // Move to next line
            x = start_x;
        } else {
            draw_char(*str, x, y, color, bg_color, scale);
            x += 8 * scale; // Move to next character position
        }
        str++;
    }
}

void clear_screen(uint32_t color) {
    if (!framebuffer) return;
    
    for (uint32_t y = 0; y < screen_height; y++) {
        for (uint32_t x = 0; x < screen_width; x++) {
            backbuffer[y * screen_pitch + x] = color;
        }
    }
}

void render_present() {
    if (!framebuffer || !backbuffer) return;

    for (uint32_t y = 0; y < screen_height; y++) {
        for (uint32_t x = 0; x < screen_width; x++) {
            framebuffer[y * screen_pitch + x] = backbuffer[y * screen_pitch + x];
        }
    }
}
